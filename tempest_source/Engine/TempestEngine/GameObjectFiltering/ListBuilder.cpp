    std::vector<std::shared_ptr<gameObject>> listBuilder::buildForPattern(componentTypeSet const& p_pattern, std::shared_ptr<scene> const & p_scene)
    {
        std::vector<std::shared_ptr<gameObject>> l_result;

		for(const auto& i_space : p_scene->getSpaces())
	        for(auto & l_go : i_space->getGameObjectList())
	        {
	            if (p_pattern <= l_go->getComponentTypeSet())
	                l_result.push_back(l_go);
	        }

        return l_result;
    }

